            supplierInfo.style.display = 'block';
            document.getElementById('supplier_name').required = true;
            document.getElementById('supplier_email').required = true;
            document.getElementById('supplier_phone').required = true;
        } else {
            supplierInfo.style.display = 'none';
            document.getElementById('supplier_name').required = false;
            document.getElementById('supplier_email').required = false;
            document.getElementById('supplier_phone').required = false;
        }
    });
</script>
